// Ejercicio 4: ¿Cuánto se ha gastado Carmen?
public class Ejercicio04 {
    public static void main(String[] args) {
        double dineroInicial = 23.0;
        double dineroRestante = 12.75;
        double dineroGastado = dineroInicial - dineroRestante;
        System.out.println("Carmen se ha gastado: " + dineroGastado + "€.");
    }
}
